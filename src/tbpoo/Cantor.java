public class Cantor {
    private String Nome;
    private int anoInicioCarreira;

    public Cantor(String Nome, int anoInicioCarreira) {
        this.Nome = Nome;
        this.anoInicioCarreira = anoInicioCarreira;
    }

   
    public String getNome() {
        return Nome;
    }

    public int getAnoInicioCarreira() {
       
        return anoInicioCarreira;
        
    }
    
    
    
    
    
}
